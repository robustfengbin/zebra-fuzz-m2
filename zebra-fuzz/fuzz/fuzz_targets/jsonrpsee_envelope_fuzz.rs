fn make_rpc(network: Network) -> FuzzRpcImpl {
    let mempool: FuzzMempool = MockMempool;
    let state = Buffer::new(MockState, 1);
    let read_state = Buffer::new(MockReadState, 1);
    let block_verifier_router: FuzzVerifier = AlwaysErr::new();
    let (_log_tx, log_rx) = tokio::sync::watch::channel(None);

    let (rpc, _queue_join) = RpcImpl::new(
        network,
        Default::default(),
        true,
        "0.0.1",
        "jsonrpsee_envelope_fuzz",
        mempool,
        state,
        read_state,
        block_verifier_router,
        AlwaysSynced::default(),
        NoChainTip,
        EmptyAddressBook::default(),
        log_rx,
        None,
    );
    drop(_queue_join);
    rpc
}

// ─────────────────────────────────────────────────────────────────────
// Process-wide singletons.
//
// `RUNTIME` — a multi-threaded tokio runtime. Multi-threaded is needed
// because `RpcImpl::new` calls `tokio::spawn` for the sendrawtransaction
// queue task, and the deeper handler paths spawn additional tasks.
// Single-thread current_thread cannot drive these without manual
// `block_on` discipline, which is fragile across iterations.
//
// `MODULE` — a single `RpcModule<FuzzRpcImpl>` produced by `into_rpc()`.
// The internal `Methods` struct is `Arc<FxHashMap>`, so cloning across
// iterations is `O(1)`. We clone it once per iteration to get a value
// that survives across the async block (the original lives in
// OnceLock<...> and `&'static` references work fine but cloning gives
// the future a 'static lifetime trivially).
// ─────────────────────────────────────────────────────────────────────

fn runtime() -> &'static tokio::runtime::Runtime {
    static RUNTIME: OnceLock<tokio::runtime::Runtime> = OnceLock::new();
    RUNTIME.get_or_init(|| {
        tokio::runtime::Builder::new_multi_thread()
            .worker_threads(2)
            .enable_all()
            .build()
            .expect("fuzz runtime must build (process-wide singleton)")
    })
}

fn module() -> &'static RpcModule<FuzzRpcImpl> {
    static MODULE: OnceLock<RpcModule<FuzzRpcImpl>> = OnceLock::new();
    MODULE.get_or_init(|| {
        // Build the rpc inside the runtime so `tokio::spawn` for the queue
        // worker has a runtime to attach to. We do this by entering the
        // runtime context for the duration of `make_rpc`.
        let _guard = runtime().enter();
        let rpc = make_rpc(Network::Mainnet);
        rpc.into_rpc()
    })
}

// ─────────────────────────────────────────────────────────────────────
// Envelope synthesis from libfuzzer bytes.
//
// Wire format:
//   data[0]            : flavour selector
//                          bit 7 (0x80): raw mode — pass remainder verbatim
//                          else        : structured mode — synthesize envelope
//   data[1] (struct.)  : method index (mod METHOD_NAMES.len())
//   data[2..] (struct.): params bytes
//
// In structured mode we attempt three params encodings, picked by
// data[1] >> 6:
//   00: params bytes interpreted as raw JSON (parse-or-string-fallback)
//   01: params wrapped as a JSON array of one string literal of the bytes
//   10: params wrapped as a JSON array of one number (8-byte big-endian)
//   11: params built from raw bytes as the JSON array body verbatim
//
// All four encodings produce a syntactically valid `params` JSON value
// (or fall back to `[]` if the encoding fails), so the envelope itself
// is always a parseable JSON object — the fuzzer's mutation pressure
// then targets the per-method `Params::parse::<T>` decoder.
//
// Note: id is fixed at 1 (number) to keep envelope parsing
// deterministic. The fuzzer can still drive the `id` decoder via the
// raw-mode flavour if it learns the structure.
// ─────────────────────────────────────────────────────────────────────

fn build_envelope(data: &[u8]) -> Option<String> {
    if data.len() < 2 {
        return None;
    }
    let method_idx = (data[1] as usize) % METHOD_NAMES.len();
    let method = METHOD_NAMES[method_idx];
    let params_kind = data[1] >> 6;
    let payload = &data[2..];

    let params_json = match params_kind {
        0b00 => {
            // Try to interpret as raw JSON; fall back to string literal.
            match std::str::from_utf8(payload) {
                Ok(s) if serde_json::from_str::<serde_json::Value>(s).is_ok() => s.to_string(),
                Ok(s) => format!("[{}]", serde_json::Value::String(s.to_string())),
                Err(_) => "[]".to_string(),
            }
        }
        0b01 => {
            let s = String::from_utf8_lossy(payload).into_owned();
            format!("[{}]", serde_json::Value::String(s))
        }
        0b10 => {
            // Numeric param: 8-byte BE → i64 then JSON number. Wider
            // surface for handlers that take Option<i32> / Option<u32>
            // (getblockhash, getnetworksolps, etc.).
            let mut buf = [0u8; 8];
            for (i, b) in payload.iter().take(8).enumerate() {
                buf[i] = *b;
            }
            let n = i64::from_be_bytes(buf);
            format!("[{}]", n)
        }
        _ => {
            // ── Per-method schema-aware param template.
            //
            // Previously this slot was "raw bytes verbatim" which produces
            // a syntactically-invalid params body 99% of the time and
            // dies at envelope-level `serde_json::from_str::<Request>`
            // instead of reaching the per-method `Params::parse::<T>`
            // surface. By selecting a template based on the method name,
            // we hand `Params::parse` a *type-shaped* body so the
            // per-method decoder runs — that's the actual surface where
            // per-method input-parsing panics live.
            //
            // The remaining fuzz bytes feed the template fields, so
            // libFuzzer can mutate around the type shape (e.g. flip a
            // bool, twiddle a number, lengthen a hex string) and reach
            // both the success path and the per-field rejection paths.
            build_method_param_template(method, payload)
        }
    };

    Some(format!(
        r#"{{"jsonrpc":"2.0","method":"{}","params":{},"id":1}}"#,
        method, params_json
    ))
}

/// Build a positional `params` JSON array tailored to the method's
/// expected signature, fed by fuzz bytes.
///
/// Falls back to `[]` if the byte budget is insufficient. The shapes
/// mirror `RpcServer` trait method signatures in `zebra-rpc/src/methods.rs`:
///
///   sendrawtransaction(hex: String, allow_high_fees: Option<bool>)
///   getblock(hash_or_height: String, verbosity: Option<u8>)
///   getblockheader(hash_or_height: String, verbose: Option<bool>)
///   getrawtransaction(txid: String, verbose: Option<u8>, block_hash: Option<String>)
///   gettxout(txid: String, n: u32, include_mempool: Option<bool>)
///   validateaddress(address: String)
///   z_validateaddress(address: String)
///   z_listunifiedreceivers(address: String)
///   getaddressbalance(req: GetAddressBalanceRequest)  // {"addresses":[...]}
///   getaddresstxids(req: GetAddressTxIdsRequest)      // {"addresses":[...], "start":N, "end":N}
///   getaddressutxos(req: GetAddressUtxosRequest)      // {"addresses":[...]} OR {"chainInfo": false, "addresses":[...]}
///   z_gettreestate(hash_or_height: String)
///   z_getsubtreesbyindex(pool: String, start_index: u16, limit: Option<u16>)
///   invalidateblock(block_hash: String)
///   reconsiderblock(block_hash: String)
///   addnode(addr: PeerSocketAddr, command: AddNodeCommand)  // ["host:port", "add"]
///   getblockhash(index: i32)
///   getblocksubsidy(height: Option<u32>)
///   getnetworksolps(num_blocks: Option<i32>, height: Option<i32>)
///   getnetworkhashps(num_blocks: Option<i32>, height: Option<i32>)
///   getrawmempool(verbose: Option<bool>)
///   submitblock(hex: HexData, _: Option<SubmitBlockParameters>)  // ["DEADBEEF"]
fn build_method_param_template(method: &str, payload: &[u8]) -> String {
    // Helper closures that pull bytes safely.
    let take_u8 = |i: usize| payload.get(i).copied().unwrap_or(0);
    let take_u32_le = |off: usize| {
        if payload.len() >= off + 4 {
            let mut b = [0u8; 4];
            b.copy_from_slice(&payload[off..off + 4]);
            u32::from_le_bytes(b)
        } else {
            0
        }
    };
    let take_i32_le = |off: usize| take_u32_le(off) as i32;
    // Hex string from bytes (truncated). The leading byte sets the length
    // class so the fuzzer can target both 32-byte (hash-shaped) and
    // tx-byte-shaped payloads.
    let hex_class = |class: u8, src: &[u8]| -> String {
        let len_target = match class & 0x03 {
            0 => 64,                              // 32-byte hash
            1 => 40,                              // 20-byte address-ish
            2 => src.len().min(64).max(2),       // arbitrary
            _ => src.len().min(2048).max(0),     // tx-byte sized hex
        };
        let raw = if src.len() >= len_target / 2 {
            &src[..len_target / 2]
        } else {
            src
        };
        hex::encode(raw)
    };

    match method {
        "sendrawtransaction" => {
            // [hex_string, optional bool]
            let hex_tx = hex_class(take_u8(0), &payload[1.min(payload.len())..]);
            let allow = (take_u8(0) >> 4) & 0x1 == 1;
            format!(
                r#"["{}",{}]"#,
                hex_tx,
                if allow { "true" } else { "false" }
            )
        }
        "submitblock" => {
            // [hex_string]
            let hex_blob = hex_class(0x80, &payload[1.min(payload.len())..]);
            format!(r#"["{}"]"#, hex_blob)
        }
        "getblock" | "getblockheader" | "z_gettreestate" => {
            // [hash_or_height_str, optional verbosity]
            // The leading byte chooses string-form: hash hex (64 chars) vs
            // numeric height vs raw JSON-string fuzz bytes.
            let s = match take_u8(0) & 0x3 {
                0 => hex_class(0, &payload[1.min(payload.len())..]),
                1 => format!("{}", take_u32_le(1) % 4_000_000),
                _ => String::from_utf8_lossy(&payload[1.min(payload.len())..32.min(payload.len())])
                    .into_owned()
                    .replace('"', ""),
            };
            let verb = (take_u8(0) >> 2) & 0x7;
            if method == "getblockheader" {
                format!(r#"["{}",{}]"#, s, verb % 2 == 1)
            } else {
                format!(r#"["{}",{}]"#, s, verb)
            }
        }
        "getrawtransaction" => {
            // [txid_hex, verbose, optional block_hash_hex]
            let txid = hex_class(0, &payload[1.min(payload.len())..]);
            let verb = (take_u8(0) >> 2) & 0x3;
            let block_hash = hex_class(0, &payload[33.min(payload.len())..]);
            format!(r#"["{}",{},"{}"]"#, txid, verb, block_hash)
        }
        "gettxout" => {
            // [txid_hex, u32 vout, optional bool include_mempool]
            let txid = hex_class(0, &payload[1.min(payload.len())..]);
            let n = take_u32_le(1) % 100;
            let include = (take_u8(0) >> 4) & 0x1 == 1;
            format!(r#"["{}",{},{}]"#, txid, n, include)
        }
        "validateaddress" | "z_validateaddress" | "z_listunifiedreceivers" => {
            // [addr_string]
            let s = String::from_utf8_lossy(&payload[..payload.len().min(96)])
                .into_owned()
                .replace('"', "");
            format!(r#"["{}"]"#, s)
        }
        "getaddressbalance" => {
            // The DTO is two-form via `from = "DGetAddressBalanceRequest"` —
            // either {"addresses":[...]} or {"address":"..."} (singular).
            // We feed the {"addresses":[...]} form so the longer parse path
            // runs.
            let addr1 = String::from_utf8_lossy(&payload[..payload.len().min(48)])
                .into_owned()
                .replace('"', "");
            format!(r#"[{{"addresses":["{}"]}}]"#, addr1)
        }
        "getaddresstxids" => {
            // {"addresses":[...], "start":N, "end":N}
            let addr1 = String::from_utf8_lossy(&payload[..payload.len().min(48)])
                .into_owned()
                .replace('"', "");
            let start = take_u32_le(50.min(payload.len()));
            let end = take_u32_le(54.min(payload.len()));
            format!(
                r#"[{{"addresses":["{}"],"start":{},"end":{}}}]"#,
                addr1, start, end
            )
        }
        "getaddressutxos" => {
            // {"addresses":[...], "chainInfo":bool}
            let addr1 = String::from_utf8_lossy(&payload[..payload.len().min(48)])
                .into_owned()
                .replace('"', "");
            let ci = take_u8(0) & 0x1 == 1;
            format!(
                r#"[{{"addresses":["{}"],"chainInfo":{}}}]"#,
                addr1, ci
            )
        }
        "z_getsubtreesbyindex" => {
            // [pool_str, u16 start, optional u16 limit]
            let pool = match take_u8(0) & 0x3 {
                0 => "sapling",
                1 => "orchard",
                2 => "sprout",
                _ => "deferred",
            };
            let start = take_u32_le(1) as u16;
            let limit = take_u32_le(5) as u16;
            format!(r#"["{}",{},{}]"#, pool, start, limit)
        }
        "invalidateblock" | "reconsiderblock" => {
            // [block_hash_hex]
            let s = hex_class(0, &payload[1.min(payload.len())..]);
            format!(r#"["{}"]"#, s)
        }
        "addnode" => {
            // [addr_str, command_str]
            // Real addrs look like "127.0.0.1:8233". Fuzz drives via the
            // parser; we put a real-shaped address with mutated port.
            let port = take_u32_le(0) % 65536;
            let cmd = match take_u8(4) & 0x1 {
                0 => "add",
                _ => "remove",
            };
            format!(r#"["127.0.0.1:{}","{}"]"#, port, cmd)
        }
        "getblockhash" => {
            // [i32 index]
            format!(r#"[{}]"#, take_i32_le(0))
        }
        "getblocksubsidy" => {
            // [optional u32 height]
            format!(r#"[{}]"#, take_u32_le(0))
        }
        "getnetworksolps" | "getnetworkhashps" => {
            // [optional i32 num_blocks, optional i32 height]
            format!(r#"[{},{}]"#, take_i32_le(0), take_i32_le(4))
        }
        "getrawmempool" => {
            // [optional bool verbose]
            let v = take_u8(0) & 0x1 == 1;
            format!(r#"[{}]"#, v)
        }
        "generate" => {
            // [u32 num_blocks]
            format!(r#"[{}]"#, take_u32_le(0) % 64)
        }
        // No-arg methods. `[]` keeps the envelope parseable.
        "getinfo" | "getblockchaininfo" | "getbestblockhash"
        | "getbestblockheightandhash" | "getblockcount" | "getmempoolinfo"
        | "getmininginfo" | "getdifficulty" | "getnetworkinfo" | "getpeerinfo"
        | "getconnectioncount" | "stop" | "ping" | "z_getnotescount"
        | "getacceptedaddrs" | "estimatefee" | "getexperimentalfeatures"
        | "help" => "[]".to_string(),
        // For methods we don't have a template for, fall back to the raw-
        // body strategy (often produces invalid JSON, exercising envelope
        // parser rejection).
        _ => {
            let body = String::from_utf8_lossy(payload).into_owned();
            format!("[{}]", body)
        }
    }
}

// ─────────────────────────────────────────────────────────────────────
// Fuzz entry point.
// ─────────────────────────────────────────────────────────────────────

fuzz_target!(|data: &[u8]| {
    if data.is_empty() {
        return;
    }
    let flavour = data[0];

    // Build the request string. Two flavours:
    //   bit 7 set → raw mode, pass remainder verbatim (or empty if empty)
    //   else      → structured mode
    let request_str = if flavour & 0x80 != 0 {
        match std::str::from_utf8(&data[1..]) {
            Ok(s) => s.to_string(),
            Err(_) => return, // can't pass non-utf8 to raw_json_request
        }
    } else {
        match build_envelope(data) {
            Some(s) => s,
            None => return,
        }
    };

    // Cap request length to prevent runaway memory in pathological
    // inputs. The production server caps at 2*MAX_BLOCK_BYTES + 1024
    // (~4 MiB); we cap lower here because our purpose is to find
    // panics in the deser surface, not memory pressure (libFuzzer has
    // its own RSS limit).
    if request_str.len() > 256 * 1024 {
        return;
    }

    let module = module();
    let runtime = runtime();

    let _ = panic::catch_unwind(AssertUnwindSafe(|| {
        runtime.block_on(async {
            let _ = module.raw_json_request(&request_str, 1).await;
        });
    }));
});
