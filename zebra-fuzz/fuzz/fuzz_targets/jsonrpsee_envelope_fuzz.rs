#![no_main]

//! Layer-2 jsonrpsee envelope deser fuzz.
//!
//! Sister harness to `rpc_handler_fuzz` (Layer-3 trait dispatch). This
//! one feeds raw JSON-RPC byte strings into
//! `jsonrpsee::RpcModule::raw_json_request(json_str, 1)` so the fuzzer
//! exercises the **envelope** layer:
//!
//!   * `serde_json::from_str::<Request>` — the envelope decoder. This
//!     parses `{"jsonrpc":"2.0","method":"...","params":[...],"id":N}`
//!     into a `jsonrpsee_types::Request`. Attacker-supplied JSON bombs
//!     (deeply-nested arrays, oversized strings, malformed UTF-8) hit
//!     this surface first.
//!   * `Methods::method(method_name)` lookup → MethodNotFound short-
//!     circuit, OR dispatch into `MethodCallback::Async` with raw
//!     `RawValue` params.
//!   * `Params::sequence()` / `Params::parse::<T>()` — the per-method
//!     param decoder generated by `#[rpc(server)]`. This is where
//!     `serde_json` deser of every method's parameter tuple happens
//!     (e.g. `(String, Option<u8>)` for `getblock`). Type confusion,
//!     wrong arity, integer overflow on i32 indices, etc. surface here
//!     **before** the handler body runs — the surface is *upstream* of
//!     what `rpc_handler_fuzz` covers.
//!
//! This harness intentionally drives `raw_json_request` with two input
//! flavours from the libfuzzer-supplied bytes:
//!
//!   1. **Structured envelope** — a small leading byte selects a
//!      method name from the production list, then we synthesize a
//!      JSON-RPC envelope `{"jsonrpc":"2.0","method":"<m>","params":<P>,"id":1}`
//!      where `<P>` is built from the remaining bytes (treated as raw
//!      JSON if it parses, or as a JSON string literal if not). This
//!      drives method-specific `Params::parse` paths.
//!   2. **Raw JSON** — when the leading byte selects "raw", the entire
//!      remaining buffer is handed to `raw_json_request` verbatim.
//!      libFuzzer can then mutate towards arbitrary attacker JSON,
//!      including malformed envelopes, missing fields, oversized
//!      params, batch flooding (jsonrpsee's HTTP layer handles
//!      batches; the `Methods::raw_json_request` API is single-call,
//!      so batches are tested at the HTTP layer in production but
//!      not here — that requires `Server::start` which we deliberately
//!      avoid to keep iterations cheap).
//!
//! All `raw_json_request` calls are wrapped in `panic::catch_unwind`
//! so that a reachable panic from any envelope or param-decoder path
//! is captured as a libFuzzer crash artifact.
//!
//! Build:
//!     cargo +nightly fuzz build jsonrpsee_envelope_fuzz
//!
//! Run (10-min cap):
//!     timeout 660 ./target/x86_64-unknown-linux-gnu/release/jsonrpsee_envelope_fuzz \
//!         -artifact_prefix=fuzz/artifacts/jsonrpsee_envelope_fuzz/ \
//!         -max_total_time=600 -print_final_stats=1 \
//!         fuzz/corpus/jsonrpsee_envelope_fuzz

