// ─────────────────────────────────────────────────────────────────────
// Mock services — copied from `rpc_handler_fuzz` (selective-Ok mocks).
//
// The enriched selective-Ok mocks are reused verbatim because they're the
// minimum surface that makes the post-await formatter code reachable
// for every method whose handler awaits state/mempool. For the envelope
// fuzz we don't actually care whether the handler returns Ok or Err —
// we only need the handler to *run* through its `Params::parse` step,
// which happens before the first await. But the Ok mocks let the
// handler proceed further, which catches more panics in formatter and
// post-decode code paths.
// ─────────────────────────────────────────────────────────────────────

struct AlwaysErr<Req, Resp> {
    _phantom: std::marker::PhantomData<fn(Req) -> Resp>,
}

impl<Req, Resp> Clone for AlwaysErr<Req, Resp> {
    fn clone(&self) -> Self {
        Self {
            _phantom: std::marker::PhantomData,
        }
    }
}

impl<Req, Resp> AlwaysErr<Req, Resp> {
    fn new() -> Self {
        Self {
            _phantom: std::marker::PhantomData,
        }
    }
}

impl<Req, Resp> Service<Req> for AlwaysErr<Req, Resp>
where
    Req: Send + 'static,
    Resp: Send + 'static,
{
    type Response = Resp;
    type Error = BoxError;
    type Future =
        Pin<Box<dyn future::Future<Output = Result<Resp, BoxError>> + Send + 'static>>;

    fn poll_ready(&mut self, _cx: &mut Context<'_>) -> Poll<Result<(), BoxError>> {
        Poll::Ready(Ok(()))
    }

    fn call(&mut self, _req: Req) -> Self::Future {
        Box::pin(async { Err::<Resp, BoxError>("fuzz mock: always-err".into()) })
    }
}

